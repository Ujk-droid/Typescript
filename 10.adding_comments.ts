// This program implements a simple calculator that performs addition and subtraction.

function add(a: number, b: number): number {
    return a + b; // Adding two numbers
}

function subtract(a: number, b: number): number {
    return a - b; // Subtracting one number from another
}

// Example usage:
console.log("Addition:", add(5, 3)); // Output: 8
console.log("Subtraction:", subtract(10, 4)); // Output: 6
